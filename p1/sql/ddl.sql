DROP TABLE IF EXISTS M_CODE;

CREATE CACHED TABLE M_CODE (
	F_TYPE VARCHAR(8) NOT NULL,
	F_CODD VARCHAR(8) NOT NULL,
	F_VALUE VARCHAR(80),
	VERSION INTEGER DEFAULT 0,
	PRIMARY KEY(F_TYPE,F_CODD)
);

DROP TABLE IF EXISTS M_SESSION;

CREATE CACHED TABLE M_SESSION (
	SESSION_ID VARCHAR(80) NOT NULL,
	DATE_START TIMESTAMP DEFAULT LOCALTIMESTAMP NOT NULL,
	DATE_END TIMESTAMP DEFAULT LOCALTIMESTAMP,
	F_USER VARCHAR(20),
	F_IP VARCHAR(20),
	F_COMMENT VARCHAR(50),
	PRIMARY KEY(SESSION_ID,DATE_START)
);

DROP TABLE IF EXISTS T_PAGE_CONDITION;

CREATE CACHED TABLE T_PAGE_CONDITION (
	SESSION_ID VARCHAR(80) NOT NULL,
	PAGE_CD VARCHAR(10) NOT NULL,
	ITEM_CD VARCHAR(50) NOT NULL,
	ITEM_VALUE VARCHAR(100),
	CREATE_USER VARCHAR(20),
	CREATE_DATE TIMESTAMP DEFAULT LOCALTIMESTAMP,
	PRIMARY KEY(SESSION_ID,PAGE_CD)
);

