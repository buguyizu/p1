package info.yinhua.core.data.model;

import java.util.Date;

public class TCustomer {

	private int id;
	private String name;
	private String bk;
	private Date dateStart;
	private Date dateEnd;
	private String mac;
	private String pwPg;
	private String pwYh;
	private Date datetime;
	/**
	 * @return the id
	 */
	public int getId() {
		return id;
	}
	/**
	 * @param id the id to set
	 */
	public void setId(int id) {
		this.id = id;
	}
	/**
	 * @return the name
	 */
	public String getName() {
		return name;
	}
	/**
	 * @param name the name to set
	 */
	public void setName(String name) {
		this.name = name;
	}
	/**
	 * @return the bk
	 */
	public String getBk() {
		return bk;
	}
	/**
	 * @param bk the bk to set
	 */
	public void setBk(String bk) {
		this.bk = bk;
	}
	/**
	 * @return the dateStart
	 */
	public Date getDateStart() {
		return dateStart;
	}
	/**
	 * @param dateStart the dateStart to set
	 */
	public void setDateStart(Date dateStart) {
		this.dateStart = dateStart;
	}
	/**
	 * @return the dateEnd
	 */
	public Date getDateEnd() {
		return dateEnd;
	}
	/**
	 * @param dateEnd the dateEnd to set
	 */
	public void setDateEnd(Date dateEnd) {
		this.dateEnd = dateEnd;
	}
	/**
	 * @return the mac
	 */
	public String getMac() {
		return mac;
	}
	/**
	 * @param mac the mac to set
	 */
	public void setMac(String mac) {
		this.mac = mac;
	}
	/**
	 * @return the pwPg
	 */
	public String getPwPg() {
		return pwPg;
	}
	/**
	 * @param pwPg the pwPg to set
	 */
	public void setPwPg(String pwPg) {
		this.pwPg = pwPg;
	}
	/**
	 * @return the pwYh
	 */
	public String getPwYh() {
		return pwYh;
	}
	/**
	 * @param pwYh the pwYh to set
	 */
	public void setPwYh(String pwYh) {
		this.pwYh = pwYh;
	}
	/**
	 * @return the datetime
	 */
	public Date getDatetime() {
		return datetime;
	}
	/**
	 * @param datetime the datetime to set
	 */
	public void setDatetime(Date datetime) {
		this.datetime = datetime;
	}
	
}
